package lett.malcolm.consciouscalculator.emulator.interfaces;

import java.util.List;
import java.util.stream.Collectors;

import lett.malcolm.consciouscalculator.utils.Events;

/**
 * Wrapper around the result from a processor.
 * 
 * Note: processors are identified by instance reference, rather than just type.
 * This is important as, in the future, there may be only a small number of processor types,
 * with many different instances constructed through learning.
 * @author Malcolm Lett
 */
// TODO also want to know which event(s) was the trigger for the processor.
// Just do this via references?
public class ProcessorResult {
	// processor which produced the emittedEvents
	private final Processor processor;
	
	// events generated by processor
	private final List<Event> emittedEvents;

	public ProcessorResult(Processor processor, List<Event> emittedEvents) {
		this.processor = processor;
		this.emittedEvents = emittedEvents;
	}
	
	@Override
	public String toString() {
		if (emittedEvents.isEmpty()) {
			return String.format("%s:{0}", processor.getClass().getSimpleName());
		}
		else {
			return String.format("%s:{%d @ %f => %s}", processor.getClass().getSimpleName(),
				emittedEvents.size(),
				emittedEvents.stream().reduce(Events.strongest()).map(e -> e.strength()).orElse(null),
				emittedEvents.stream().map(e -> e.getClass().getSimpleName()).collect(Collectors.toList()));
		}
	}

	public Processor processor() {
		return processor;
	}

	public List<Event> emittedEvents() {
		return emittedEvents;
	}
}
